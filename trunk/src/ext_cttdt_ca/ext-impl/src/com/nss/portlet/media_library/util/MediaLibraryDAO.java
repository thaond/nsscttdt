package com.nss.portlet.media_library.util;

public class MediaLibraryDAO {
	private String tenFile;
	private long kichThuocFile;
	private String path;
	public MediaLibraryDAO() {
		
	}
	public String getTenFile() {
		return tenFile;
	}
	public void setTenFile(String tenFile) {
		this.tenFile = tenFile;
	}
	public long getKichThuocFile() {
		return kichThuocFile;
	}
	public void setKichThuocFile(long kichThuocFile) {
		this.kichThuocFile = kichThuocFile;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	
}
